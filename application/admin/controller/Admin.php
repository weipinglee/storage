namespace app\admin\Controller;

use think\Request;
class Admin extends Base
{

    protected $serviceModel = null;
    public function _initialize()
    {
        parent::_initialize(); // TODO: Change the autogenerated stub
        $this->serviceModel = \think\Loader::model('Admin','service');
        $this->view->engine->layout('layout/layout');
    }

    public function lst()
    {
        $model = $this->serviceModel;
        $page = isset($_GET['page']) ? $_GET['page'] : 1 ;
        $data = $model->lists($page);
        $this->assign(
            'data',$data['data']
        );
        $this->assign('pageData',$data['page']);

        // 设置页面中的信息
        $this->assign(array(
            '_page_btn_name' => '添加管理员',
            '_page_btn_link' => url('add'),
        ));
        return $this->fetch();
    }
    public function add()
    {
        $request = Request::instance();
        $res = $this->serviceModel->add($request->param());
        die(json_encode($res));

    }
    public function edit()
    {
        $request = Request::instance();
        $id = $request->param('id');
        $data = $request->param();
        $res = $this->serviceModel->edit($id,$data);
        die(json_encode($res));
    }
    public function delete()
    {
        $request = Request::instance();
        $res = $this->serviceModel->delete($request->param('id'));
        die(json_encode($res));
    }
}